NewsApp.controller('AuthCtrl', ['$scope','$routeParams', function ($scope, $routeParams) {

  $scope.user = {};

  $scope.register = function(){
    auth.register($scope.user).error(function(error){
      $scope.error = error;
    }).then(function(){
      $state.go('/');
    });
  };

  $scope.logIn = function(){
    auth.logIn($scope.user).error(function(error){
      $scope.error = error;
    }).then(function(){
      $state.go('/');
    })
  }

}]);